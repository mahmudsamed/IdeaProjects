package day13whileloopdownwhileloop;

public class DoWhileLoop01 {

    //1)while- loop
    public static void main(String[] args) {

        int i =1;
        while (i<1){
            System.out.println("Sen bir while loopsun");
            i++;
        }

        //2)do while loop
        int k=1;

        do {
            System.out.println("Sen bir do while loopsun");

            k++;
        }while (k<1);









    }






}
